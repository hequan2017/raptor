<script setup>
import {
  createService,
  deleteService,
  deleteServiceByIds,
  updateService,
  findService,
  getServiceList
} from '@/api/service'

// 全量引入格式化工具 请按需保留
import { getDictFunc, formatDate, formatBoolean, filterDict,formatProduct } from '@/utils/format'
import { ElMessage, ElMessageBox } from 'element-plus'
import { ref } from 'vue'
import {getProductList} from "@/api/product";
import { useRouter } from 'vue-router'
const router = useRouter()
// 自动化生成的字典（可能为空）以及字段
const formData = ref({
        name: '',
        type: '',
        url: '',
        branch: '',
        start: '',
        stop: '',
        activity: '',
        env: '',
        shell: '',
        products:[],
        other: '',
        })

// =========== 表格控制部分 ===========
const page = ref(1)
const total = ref(0)
const pageSize = ref(10)
const tableData = ref([])
const searchInfo = ref({})
const productsOptions = ref([])
const typeOptions = ref( [{
  "label": "prod",
  "value": "prod"
}, {
  "label": "test",
  "value": "test"
}])

const getProductsOptions = async() => {
  const table = await getProductList({ page: page.value, pageSize: 1000})
  if (table.code === 0) {
    table.data.list.forEach(function(val, index, arr){
      productsOptions.value.push(val)
    });
  }
}

// 重置
const onReset = () => {
  searchInfo.value = {}
}

// 搜索
const onSubmit = () => {
  page.value = 1
  pageSize.value = 10
  getTableData()
}

// 分页
const handleSizeChange = (val) => {
  pageSize.value = val
  getTableData()
}

// 修改页面容量
const handleCurrentChange = (val) => {
  page.value = val
  getTableData()
}

// 查询
const getTableData = async() => {
  const table = await getServiceList({ page: page.value, pageSize: pageSize.value, ...searchInfo.value })
  if (table.code === 0) {
    tableData.value = table.data.list
    total.value = table.data.total
    page.value = table.data.page
    pageSize.value = table.data.pageSize
  }
}

getTableData()

// ============== 表格控制部分结束 ===============

// 获取需要的字典 可能为空 按需保留
const setOptions = async () =>{
}

// 获取需要的字典 可能为空 按需保留
setOptions()


// 多选数据
const multipleSelection = ref([])
// 多选
const handleSelectionChange = (val) => {
    multipleSelection.value = val
}

// 删除行
const deleteRow = (row) => {
    ElMessageBox.confirm('确定要删除吗?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
    }).then(() => {
            deleteServiceFunc(row)
        })
    }


// 批量删除控制标记
const deleteVisible = ref(false)

// 多选删除
const onDelete = async() => {
      const ids = []
      if (multipleSelection.value.length === 0) {
        ElMessage({
          type: 'warning',
          message: '请选择要删除的数据'
        })
        return
      }
      multipleSelection.value &&
        multipleSelection.value.map(item => {
          ids.push(item.ID)
        })
      const res = await deleteServiceByIds({ ids })
      if (res.code === 0) {
        ElMessage({
          type: 'success',
          message: '删除成功'
        })
        if (tableData.value.length === ids.length && page.value > 1) {
          page.value--
        }
        deleteVisible.value = false
        getTableData()
      }
    }

// 行为控制标记（弹窗内部需要增还是改）
const type = ref('')

// 更新行
const updateServiceFunc = async(row) => {
  getProductsOptions()
    const res = await findService({ ID: row.ID })
    type.value = 'update'
    if (res.code === 0) {
        formData.value = res.data.reservice
        dialogFormVisible.value = true
    }
}

const buildServiceFunc = async(row) => {
  await router.push({path: `/layout/serve/serviceBuildCreate`, query: {id: row.ID}})
}



// 删除行
const deleteServiceFunc = async (row) => {
    const res = await deleteService({ ID: row.ID })
    if (res.code === 0) {
        ElMessage({
                type: 'success',
                message: '删除成功'
            })
            if (tableData.value.length === 1 && page.value > 1) {
            page.value--
        }
        getTableData()
    }
}

// 弹窗控制标记
const dialogFormVisible = ref(false)

// 打开弹窗
const openDialog = () => {
  getProductsOptions()
    type.value = 'create'
    dialogFormVisible.value = true
}

// 关闭弹窗
const closeDialog = () => {
    dialogFormVisible.value = false
    formData.value = {
        name: '',
        type: '',
        url: '',
        branch: '',
        start: '',
        stop: '',
      products:[],
        activity: '',
        env: '',
        shell: '',
        other: '',
        }
}
// 弹窗确定
const enterDialog = async () => {
      let res
      switch (type.value) {
        case 'create':
          res = await createService(formData.value)
          break
        case 'update':
          res = await updateService(formData.value)
          break
        default:
          res = await createService(formData.value)
          break
      }
      if (res.code === 0) {
        ElMessage({
          type: 'success',
          message: '创建/更改成功'
        })
        closeDialog()
        getTableData()
      }
}
</script>

